package Switch_Case;
//f the character is A, E, I, O, or U, it is vowel otherwise consonant
public class EXAMPLE3 
{
	public static void main(String[] args)
	{
		char ch='q';
		switch(ch)
		{
		case 'a':
		{
			System.out.println("vovel");
			break;
		}
		case 'e':
		{
			System.out.println("vovel");
			break;
		}
		case 'i':
		{
			System.out.println("vovel");
			break;
		}
		
		case 'o':
		{
			System.out.println("vovel");
			break;
		}
		case 'u':
		{
			System.out.println("vovel");
			break;
		}
		default:
		{
			System.out.println("Not Vovel");
		}
		}
		
	}
}
