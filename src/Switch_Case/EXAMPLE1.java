package Switch_Case;

public class EXAMPLE1 
{
	public static void main(String[] args) 
	{
		int num=19;
		switch(num) {
		case 10:System.out.println("Number is 10");
		break;
		case 12:System.out.println("Number is 12");
		break;
		case 15:System.out.println("Number is 15");
		break;
		case 17:System.out.println("Number is 17");
		break;
		case 19:System.out.println("Number is 19");
		break;
		}
	}
}
